package S2_DataType_Var_lit;

public class literals {

    public static void main(String[] args) {
        byte a = 10;
        byte b = 0b1010;
        byte c = 012;
        byte d = 0xA;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

        int p = 9;
        int q = 5;
        System.out.println(p + q);

    }

}
